# Mendefinisikan variabel dengan nilai string
nama = "Raihan Fadhli Ramadhan"
umur = 20

# Menampilkan nilai variabel
print("Nama:", nama)
print("Umur:", umur)

# Mengubah nilai variabel
umur = 35

# Menampilkan nilai variabel yang telah diubah
print("Umur sekarang:", umur)

# Menggunakan variabel dalam ekspresi matematika
panjang = 10
lebar = 5
luas = panjang * lebar

# Menampilkan hasil dari ekspresi matematika yang menggunakan variabel
print("Luas persegi panjang:", luas)
